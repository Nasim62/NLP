# Q3. You can use a minus sign to make a negative number like -2. What happens if you put a plus sign before a number? What about 2++2?
# Ans.If you put a plus sign before a number, Python will interpret it as a positive number, so +2 is the same as 2. 
#     If you have 2++2, Python will interpret it as 2 + (+2), which equals 4. Example:
print(2++2)
#output:4